class MageWorx_CustomOptions_Block_Adminhtml_Reports_Renderer_Qty extends Mage_Adminhtml_Block_Widget_Grid_Column_Renderer_Abstract
{

    public function render(Varien_Object $row)
    {
        $helper = Mage::helper('mageworx_customoptions');
        $qty = $this->_getValue($row);

        return $helper->removeQtyZeroes($qty).'';
    }

}
